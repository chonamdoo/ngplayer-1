package com.nd2h.ngplayer;

public class DisplaySize {
	private float realScreenWidth ;
	private float realScreenHeight ;
	
	
	
	public float getRealScreenWidth() {
		return realScreenWidth;
	}
	public void setRealScreenWidth(float realScreenWidth) {
		this.realScreenWidth = realScreenWidth;
	}
	public float getRealScreenHeight() {
		return realScreenHeight;
	}
	public void setRealScreenHeight(float realScreenHeight) {
		this.realScreenHeight = realScreenHeight;
	}
	
	
}
